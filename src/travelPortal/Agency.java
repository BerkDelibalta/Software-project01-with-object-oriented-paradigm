package travelPortal;


public class Agency {

	private String name;
	private String activity;

	public Agency(String name2, String activity) {
		this.name = name2;
		this.activity = activity;
	}

	public String getName() {
		return name;
	}

	public String getActivity() {
		return activity;
	}
	
	
}
